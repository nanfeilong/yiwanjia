package cn.junyun.mall.model;
/**
 * 收货地址管理的实体类
 * @author NanFeiLong
 * @date 2016年8月24日
 */
public class AddressManageEntity {
	
	//收货人姓名
	private String ConsigneeNameTv;
	//收货人电话
	private String ConsigneePhoneNumberTv;
	//收货人地址
	private String ConsigneeAddressTv;
	//是否是默认地址
	private boolean CheckBoxAddressBl;

	public AddressManageEntity(String consigneeNameTv,
			String consigneePhoneNumberTv, String consigneeAddressTv,
			boolean checkBoxAddressBl) {
		super();
		ConsigneeNameTv = consigneeNameTv;
		ConsigneePhoneNumberTv = consigneePhoneNumberTv;
		ConsigneeAddressTv = consigneeAddressTv;
		CheckBoxAddressBl = checkBoxAddressBl;
	}

	public String getConsigneeNameTv() {
		return ConsigneeNameTv;
	}

	public void setConsigneeNameTv(String consigneeNameTv) {
		ConsigneeNameTv = consigneeNameTv;
	}

	public String getConsigneePhoneNumberTv() {
		return ConsigneePhoneNumberTv;
	}

	public void setConsigneePhoneNumberTv(String consigneePhoneNumberTv) {
		ConsigneePhoneNumberTv = consigneePhoneNumberTv;
	}

	public String getConsigneeAddressTv() {
		return ConsigneeAddressTv;
	}

	public void setConsigneeAddressTv(String consigneeAddressTv) {
		ConsigneeAddressTv = consigneeAddressTv;
	}

	public boolean isCheckBoxAddressBl() {
		return CheckBoxAddressBl;
	}

	public void setCheckBoxAddressBl(boolean checkBoxAddressBl) {
		CheckBoxAddressBl = checkBoxAddressBl;
	}

	@Override
	public String toString() {
		return "AddressManageEntity [ConsigneeNameTv=" + ConsigneeNameTv
				+ ", ConsigneePhoneNumberTv=" + ConsigneePhoneNumberTv
				+ ", ConsigneeAddressTv=" + ConsigneeAddressTv
				+ ", CheckBoxAddressBl=" + CheckBoxAddressBl + "]";
	}










}
