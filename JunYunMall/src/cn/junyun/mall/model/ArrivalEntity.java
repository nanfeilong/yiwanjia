package cn.junyun.mall.model;
//新品上新entity
public class ArrivalEntity extends Entity {
	//背景图片
	private int arrivalItemIv;
	//底部文字
	private String arrivalTextView;
	public ArrivalEntity() {
		super();
	}
	public ArrivalEntity(int arrivalItemIv, String arrivalTextView) {
		super();
		this.arrivalItemIv = arrivalItemIv;
		this.arrivalTextView = arrivalTextView;
	}
	public int getArrivalItemIv() {
		return arrivalItemIv;
	}
	public void setArrivalItemIv(int arrivalItemIv) {
		this.arrivalItemIv = arrivalItemIv;
	}
	public String getArrivalTextView() {
		return arrivalTextView;
	}
	public void setArrivalTextView(String arrivalTextView) {
		this.arrivalTextView = arrivalTextView;
	}
	@Override
	public String toString() {
		return "ArrivalEntity [arrivalItemIv=" + arrivalItemIv
				+ ", arrivalTextView=" + arrivalTextView + "]";
	}

}
