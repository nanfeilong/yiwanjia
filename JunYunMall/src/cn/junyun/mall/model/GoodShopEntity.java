package cn.junyun.mall.model;

public class GoodShopEntity extends Entity{
	private int goodShopLogo;
	private String shopName;
	private int goodShopVip;
	private int goodShopLeftIv;
	private int goodShopRightIv1;
	private int goodShopRightIv2;
	public GoodShopEntity() {
		super();
	}
	public GoodShopEntity(int goodShopLogn, String shopName, int goodShopVip,
			int goodShopLeftIv, int goodShopRightIv1, int goodShopRightIv2) {
		super();
		this.goodShopLogo = goodShopLogn;
		this.shopName = shopName;
		this.goodShopVip = goodShopVip;
		this.goodShopLeftIv = goodShopLeftIv;
		this.goodShopRightIv1 = goodShopRightIv1;
		this.goodShopRightIv2 = goodShopRightIv2;
	}
	public int getGoodShopLogn() {
		return goodShopLogo;
	}
	public void setGoodShopLogn(int goodShopLogn) {
		this.goodShopLogo = goodShopLogn;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public int getGoodShopVip() {
		return goodShopVip;
	}
	public void setGoodShopVip(int goodShopVip) {
		this.goodShopVip = goodShopVip;
	}
	public int getGoodShopLeftIv() {
		return goodShopLeftIv;
	}
	public void setGoodShopLeftIv(int goodShopLeftIv) {
		this.goodShopLeftIv = goodShopLeftIv;
	}
	public int getGoodShopRightIv1() {
		return goodShopRightIv1;
	}
	public void setGoodShopRightIv1(int goodShopRightIv1) {
		this.goodShopRightIv1 = goodShopRightIv1;
	}
	public int getGoodShopRightIv2() {
		return goodShopRightIv2;
	}
	public void setGoodShopRightIv2(int goodShopRightIv2) {
		this.goodShopRightIv2 = goodShopRightIv2;
	}
	@Override
	public String toString() {
		return "GoodShopEntity [goodShopLogn=" + goodShopLogo + ", shopName="
				+ shopName + ", goodShopVip=" + goodShopVip
				+ ", goodShopLeftIv=" + goodShopLeftIv + ", goodShopRightIv1="
				+ goodShopRightIv1 + ", goodShopRightIv2=" + goodShopRightIv2
				+ "]";
	}

}
