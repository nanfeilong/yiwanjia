package cn.junyun.mall.model;
//精品推荐
public class RecommendEntity extends Entity{
	//精品推荐图片
	private int recommendIv;
	//精品推荐标题
	private String recommendTitleTv;
	//精品推荐内容
	private String recommendDetails;
	//喜欢本商品人数
	private String recommendSayGoodNumber;
	public RecommendEntity(int recommendIv, String recommendTitleTv,
			String recommendDetails, String recommendSayGoodNumber) {
		super();
		this.recommendIv = recommendIv;
		this.recommendTitleTv = recommendTitleTv;
		this.recommendDetails = recommendDetails;
		this.recommendSayGoodNumber = recommendSayGoodNumber;
	}
	public RecommendEntity() {
		super();
	}
	public int getRecommendIv() {
		return recommendIv;
	}
	public void setRecommendIv(int recommendIv) {
		this.recommendIv = recommendIv;
	}
	public String getRecommendTitleTv() {
		return recommendTitleTv;
	}
	public void setRecommendTitleTv(String recommendTitleTv) {
		this.recommendTitleTv = recommendTitleTv;
	}
	public String getRecommendDetails() {
		return recommendDetails;
	}
	public void setRecommendDetails(String recommendDetails) {
		this.recommendDetails = recommendDetails;
	}
	public String getRecommendSayGoodNumber() {
		return recommendSayGoodNumber;
	}
	public void setRecommendSayGoodNumber(String recommendSayGoodNumber) {
		this.recommendSayGoodNumber = recommendSayGoodNumber;
	}
	@Override
	public String toString() {
		return "RecommendEntity [recommendIv=" + recommendIv
				+ ", recommendTitleTv=" + recommendTitleTv
				+ ", recommendDetails=" + recommendDetails
				+ ", recommendSayGoodNumber=" + recommendSayGoodNumber + "]";
	}

}
