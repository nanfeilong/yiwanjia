package cn.junyun.mall.model;

import java.util.ArrayList;

/**
 * 确认订单实体类
 * wang
 */

public class MakeSureOrderEntity  extends Entity{
	
	private String entitySHopName;//店铺名
	private String courierName;//快递名
	private ArrayList<MakeSureOrderInnerEntity> makeSureOrderInners;
	
	
	public MakeSureOrderEntity(String entitySHopName, String courierName,
			ArrayList<MakeSureOrderInnerEntity> makeSureOrderInners) {
		super();
		this.entitySHopName = entitySHopName;
		this.courierName = courierName;
		this.makeSureOrderInners = makeSureOrderInners;
	}
	public String getEntitySHopName() {
		return entitySHopName;
	}
	public void setEntitySHopName(String entitySHopName) {
		this.entitySHopName = entitySHopName;
	}
	public String getCourierName() {
		return courierName;
	}
	public void setCourierName(String courierName) {
		this.courierName = courierName;
	}
	public ArrayList<MakeSureOrderInnerEntity> getMakeSureOrderInners() {
		return makeSureOrderInners;
	}
	public void setMakeSureOrderInners(
			ArrayList<MakeSureOrderInnerEntity> makeSureOrderInners) {
		this.makeSureOrderInners = makeSureOrderInners;
	}
	
	

}
